import turtle
turtle = turtle.Turtle()
# function defination
def square():
  turtle.forward(100)
  turtle.left(90)
  turtle.forward(100)
  turtle.left(90)
  turtle.forward(100)
  turtle.left(90)
  turtle.forward(100)
  turtle.left(90)

# function call
square()

